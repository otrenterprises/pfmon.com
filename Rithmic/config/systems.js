export function findSystem(serverName) {
    return systems.systems.find(sys => sys.server === serverName);
}

// Helper function to find gateway by system and gateway name
export function findGateway(serverName, gatewayName) {
    const system = findSystem(serverName);
    if (!system) return null;
    
    return system.gateways.find(gw => gw.name === gatewayName);
}